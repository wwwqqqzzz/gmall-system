<!--注册页面-->
<template>
  <div class="wrapper">
    <div style="margin: 200px auto;background-color:#fff;width: 350px;height: 400px;padding: 20px;border-radius: 10px">
      <div style="margin: 20px 0;text-align: center; font-size: 24px"><b>GMall后台登录</b></div>
      <el-form :model="user" :rules="rules" ref="userForm">
        <el-form-item prop="username">
          <el-input size="medium" placeholder="请输入账号" style="margin: 10px 0" prefix-icon="el-icon-user"  v-model="user.username" ></el-input>
        </el-form-item>
        <el-form-item  prop="password">
          <el-input size="medium"  placeholder="请输入密码" style="margin: 10px 0" prefix-icon="el-icon-lock" show-password v-model="user.password"></el-input>
        </el-form-item>
        <el-form-item  prop="confirmPassword">
          <el-input size="medium" placeholder="请确认密码" style="margin: 10px 0" prefix-icon="el-icon-lock" show-password v-model="user.confirmPassword"></el-input>
        </el-form-item>
      </el-form>
      <div style="margin: 10px 0;text-align: right">
        <el-button type="primary" size="small" autocomplete="off" @click="login">注册</el-button>
        <el-button type="warning" size="small" autocomplete="off" @click="$router.push('/login')">返回登录</el-button>
      </div>
    </div>
  </div>

</template>

<script>
export default {
  name: "RegisterView",
  data(){
    return{
      user:{},
      rules: {
        username: [
          {required: true, message: '请输入用户名', trigger: 'blur'},
          {min: 3, max: 5, message: '长度在 3 到 5 个字符', trigger: 'blur'}
        ],
        password: [
          {required: true, message: '请输入输入密码', trigger: 'blur'},
          {min: 3, max: 20, message: '长度在 1 到 20 个字符', trigger: 'blur'}
        ],
        confirmPassword: [
          {required: true, message: '请确认密码', trigger: 'blur'},
          {min: 3, max: 20, message: '长度在 1 到 20 个字符', trigger: 'blur'}
        ]
      }
    }
  },
  methods:{
    login(){
      this.$refs['userForm'].validate((valid) => {
        if (valid) {
          if (this.user.password != this.user.confirmPassword){
            this.$message.success("两次输入密码不一致")
            return false
          }
          //表单校验
          this.request.post("/user/register",this.user).then(res => {
            if (res.code === '200'){
              this.$message.success("注册成功")
            }else {
              this.$message.error(res.msg)
            }
          })
        } else {
          return false;
        }
      });
    }
  }
}
</script>

<style scoped>
  .wrapper{
    height: 100vh;
    background-image:linear-gradient(to bottom right,#FC466B,#3F5EFB);
    overflow: hidden;
  }
</style>